const messages: Record<Locale, Record<Key, string>> = { en, ru, kk };

export function bybitUtaText(locale: Locale, key: Key, values: Record<string, string> = {}): string {
  let value = messages[locale][key] ?? en[key];
  for (const [name, replacement] of Object.entries(values)) value = value.replaceAll(`{${name}}`, replacement);
  return value;
}
