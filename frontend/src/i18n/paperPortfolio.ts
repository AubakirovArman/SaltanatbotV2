export function paperPortfolioText(locale: Locale, key: PaperPortfolioMessageKey): string {
  return messages[locale][key];
}

export function paperRobotStatusText(locale: Locale, status: PaperRobotControlStatus | undefined): string {
  return paperPortfolioText(locale, status ?? "idle");
}

export function paperRobotActionText(locale: Locale, action: PaperRobotAction): string {
  return paperPortfolioText(locale, `action${action[0].toUpperCase()}${action.slice(1)}` as "actionStart" | "actionPause" | "actionResume" | "actionStop");
}

export function paperLedgerEventText(locale: Locale, type: PaperLedgerEventType): string {
  return ledgerEvents[locale][type];
}
