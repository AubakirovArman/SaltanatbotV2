import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const legacy = JSON.parse(readFileSync(path.join(root, "frontend/tests/pineCorpus.json"), "utf8"));
const modern = JSON.parse(readFileSync(path.join(root, "frontend/tests/pineV6Corpus.json"), "utf8"));
const tsPath = path.join(root, "frontend/src/strategy/pine/generatedCompatibility.ts");
const docPath = path.join(root, "docs/PINE_COMPATIBILITY.generated.md");
const rank = { exact: 0, "display-only": 1, approximation: 2, rejected: 3 };
const rows = new Map();

for (const script of legacy.scripts) {
  for (const feature of script.mustMap ?? []) add(feature, "exact", script.title, "v4/v5 corpus");
  for (const feature of script.mayWarn ?? []) add(feature, classify(feature), script.title, "v4/v5 corpus");
}
for (const script of modern.scripts) {
  const rejected = /\bREJECT\b/i.test(script.title);
  for (const feature of script.features ?? []) {
    const level = rejected && /\breject\b|look-ahead|future offset|pivot/i.test(feature) ? "rejected" : classify(feature);
    add(feature, level, script.title, "v6 corpus");
  }
  if (rejected) add(`Rejected program: ${script.title.replace(/\s*\(REJECT.*$/i, "")}`, "rejected", script.title, "v6 corpus");
}

const registry = [...rows.values()]
  .map((row) => ({ ...row, scripts: [...row.scripts].sort(), corpora: [...row.corpora].sort() }))
  .sort((a, b) => rank[b.level] - rank[a.level] || a.feature.localeCompare(b.feature));
const summary = Object.fromEntries(Object.keys(rank).map((level) => [level, registry.filter((row) => row.level === level).length]));

const ts = `// Generated by scripts/generate-pine-compatibility.mjs — do not edit.\n` +
`export type PineCompatibilityLevel = "exact" | "display-only" | "approximation" | "rejected";\n\n` +
`export interface PineCompatibilityEntry {\n  feature: string;\n  level: PineCompatibilityLevel;\n  scripts: readonly string[];\n  corpora: readonly string[];\n}\n\n` +
`export const PINE_COMPATIBILITY_REGISTRY = [\n${registry.map((row) => `  ${JSON.stringify(row)},`).join("\n")}\n] as const satisfies readonly PineCompatibilityEntry[];\n\n` +
`export const PINE_COMPATIBILITY_SUMMARY = ${JSON.stringify(summary, null, 2)} as const;\n`;

const doc = `# Generated Pine compatibility matrix\n\n` +
`> Generated from \`frontend/tests/pineCorpus.json\` and \`frontend/tests/pineV6Corpus.json\`. Do not edit by hand.\n\n` +
`This matrix describes tested corpus features, not full Pine Script compatibility. **Approximation** and **display-only** rows require reviewing import diagnostics before using an artifact for trading.\n\n` +
`| Level | Features |\n| --- | ---: |\n` +
Object.entries(summary).map(([level, count]) => `| ${level} | ${count} |`).join("\n") +
`\n\n| Feature | Level | Corpus | Covered by scripts |\n| --- | --- | --- | --- |\n` +
registry.map((row) => `| ${escapeCell(row.feature)} | ${row.level} | ${row.corpora.join(", ")} | ${escapeCell(row.scripts.join(", "))} |`).join("\n") + "\n";

if (process.argv.includes("--check")) {
  const stale = [[tsPath, ts], [docPath, doc]].filter(([file, expected]) => safeRead(file) !== expected);
  if (stale.length > 0) {
    console.error(`Generated Pine compatibility files are stale: ${stale.map(([file]) => path.relative(root, file)).join(", ")}`);
    process.exit(1);
  }
  console.log(`Pine compatibility registry is current (${registry.length} feature rows).`);
} else {
  writeFileSync(tsPath, ts);
  writeFileSync(docPath, doc);
  console.log(`Generated ${registry.length} Pine compatibility rows.`);
}

function add(rawFeature, level, script, corpus) {
  const feature = String(rawFeature).replace(/\s+/g, " ").trim();
  if (!feature) return;
  const key = feature.toLowerCase();
  const current = rows.get(key);
  if (!current) {
    rows.set(key, { feature, level, scripts: new Set([script]), corpora: new Set([corpus]) });
    return;
  }
  if (rank[level] > rank[current.level]) current.level = level;
  current.scripts.add(script);
  current.corpora.add(corpus);
}

function classify(feature) {
  const text = String(feature).toLowerCase();
  if (/\breject\b|look-ahead offset|future offset/.test(text)) return "rejected";
  if (/approx|opaque|recursive|request\.security|\bmtf\b|external-series|collection|array|matrix|map|unsupported|ignored|dropped|sign-only|single-position/.test(text)) return "approximation";
  if (/\bplot|fill\(|drawing|label|line|box|table|color|style|histogram|background|bgcolor|barcolor|shape|hline/.test(text)) return "display-only";
  return "exact";
}

function escapeCell(value) {
  return String(value).replace(/\|/g, "\\|").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

function safeRead(file) {
  try { return readFileSync(file, "utf8"); } catch { return undefined; }
}
